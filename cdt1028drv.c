// 
//  Params:
// 	dwVFreq    : Desired tuning frequency in hertz.   i.e. 57.75mhz  =
// 				57750000.
//  eVideoStd  : AnalogTV standard set.
//  Return:
// 	CDTMTV_RT_ERROR:     an error occurred communicating with the device.  
// 	CDTMTV_RT_SCAN_FAIL: not found channel.
// 	CDTMTV_RT_SCAN_DONE: found channel.
//  --------------------------------------------------------------------------- 
uint8 CDT1028_FM_ScanChannel(uint32 dwVFreq ) LARGE
{
	uint8 bStatus;
	 // uint8 cnt=5;

	 // uint8 u8test = 20, u8i, u8cnt;
	tuner_cfg_g.tv_mode_index = RDA_DTV_6M;
	rdaTVSetFreq_tune(dwVFreq,0,&tuner_cfg_g);

	RDA_DELAY_MS(20);
	bStatus=rdaFmSeekConfirm();
	if(1==bStatus)
		bStatus=CDTMTV_RT_SCAN_DONE;
	else
		bStatus=CDTMTV_RT_SCAN_FAIL;
	return bStatus;
}
