U8 rdaScanFmConfirm(U8 fm_valid)LARGE
{
    U8 i;
    U8 cfm_stard=0;
    U16 wratio = 0,cratio=0,tmp= 0;
    U32 average=0;
	#ifdef SEEK_DEBUG
    U32 addr_tmp;
    addr_tmp = SCU_RAM_SEEK_FM_BASE-1;
	#endif

    for(i=0;i<fm_valid;i++)
    {
        wratio = 0;
        tmp = 255 - fm_param[i].fm_afc;
        tmp = tmp*35;
        wratio += tmp;
        tmp = fm_param[i].fm_signal;
        tmp = tmp<<1;
        tmp = tmp*15;
        wratio += tmp;
        tmp = fm_param[i].fm_noise;
        tmp = tmp<<1;
        tmp = 255-tmp;
        tmp = tmp*50;
        wratio += tmp;
        average += wratio;

	#ifdef SEEK_DEBUG
        addr_tmp += 4;
        i2c1SendDataA(addr_tmp,wratio);
	#endif
        if(wratio > cratio)
        {
            cratio = wratio;
			cfm_stard = i;
        }
        else if(wratio == cratio)
        {
            if(fm_param[i].fm_noise >= fm_param[i-1].fm_noise)
            {
                cfm_stard = i-1;
            }
            else
            {
                cfm_stard = i;
            }
        }
        else
        {
            continue;
        }

    }

    average = average>>2;
    average = wratio-average;

    if(average <= 50)
    {
        return FM_NULL;
    }
    return cfm_stard;
}
