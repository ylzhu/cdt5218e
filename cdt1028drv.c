uint32 rdaTVMulDivCompensate(uint32 par1,uint32 par2,uint32 par3)LARGE
{
	uint32 result=0;
	uint32 div_int,div_remain,div_remain1;
	uint32 mremain,mb,fremain;

	div_int = par1/par2;
	div_remain = par1%par2;

	div_int = div_int<<par3;
	result = result + div_int;

	// 	mremain = ((U32)1<<(32-par3))-1;
	par1 = 32-par3;
	mremain = ((U32)1)<<par1;
	mremain = mremain-1;     // end

	mb = div_remain/mremain;
	fremain = div_remain%mremain;

	// 	div_int = (fremain<<par3)/par2;
	par1 = fremain<<par3;
	div_int = par1/par2;     // end

	result = result + div_int;

	// 	div_remain1 = (fremain<<par3)%par2;
	par1 = fremain<<par3;
	div_remain1 = par1%par2;     // end

	// 	div_int = (mremain<<par3)/par2;
	par1 = mremain<<par3;
	div_int = par1/par2;     // end

	div_int = div_int*mb;
	result = result + div_int;

	// 	div_remain = (mremain<<par3)%par2;
	par1 = mremain<<par3;
	div_remain = par1%par2;     // end

	div_remain = div_remain*mb;
	div_remain = div_remain + div_remain1;

	div_int = div_remain/par2;
	result = result + div_int;

	div_remain1 = div_remain%par2;
	div_remain1 = div_remain1<<1;

	if(div_remain1 >= par2)
	result += 1;

	return result;

}

