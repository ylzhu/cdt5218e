uint16 i2c0GetData(uint8 addr)LARGE
{
	uint8 tdata[2];
	uint16 rdata = 0;
	uint8 err = 0;
	err = cdt_r_reg(RDA5880DM_CHIP_ID_0, &addr, 1, tdata, 2);
	rdata = tdata[0] << 8;
	rdata |= tdata[1];
	return rdata;
}

void i2c0SendData(uint8 addr, uint16 rdata)LARGE
{
	uint8 pdata[2] = {0};
	uint8 err;
	pdata[0] = rdata>>8&0xff;
	pdata[1] = rdata&0xff;
	err = cdt_w_reg(RDA5880DM_CHIP_ID_0, &addr, 1, pdata, 2);
}

void i2c1SendDataA(uint32 addr, uint16 wdata)LARGE
{   
	uint8 paddr[4];
	uint8 pdata[2];
	uint8 err;

	paddr[0] = (uint8)(addr<<1|0x01);
	paddr[1] = (uint8)(addr>>16);
	paddr[2] = (uint8)(addr>>24);
	paddr[3] = (uint8)(addr>>7);
	pdata[0] = (uint8)wdata;
	pdata[1] = (uint8)(wdata>>8);

	err = cdt_w_reg(RDA5880DM_CHIP_ID_1, paddr, 4, pdata, 2);
}

void i2c1SendData(uint16 page, uint32 offset, uint16 wdata)LARGE
{
	uint32 addr;
	uint8 paddr[4];
	uint8 pdata[2];
	uint8 err;
	addr = page;
	addr <<= 20;
	addr |= offset;
	paddr[0] = (uint8)(addr<<1|0x01);
	paddr[1] = (uint8)(addr>>16);
	paddr[2] = (uint8)(addr>>24);
	paddr[3] = (uint8)(addr>>7);
	pdata[0] = (uint8)wdata;
	pdata[1] = (uint8)(wdata>>8);

	err = cdt_w_reg(RDA5880DM_CHIP_ID_1, paddr, 4, pdata, 2);
}

uint16 i2c1GetData(uint16 page, uint32 offset)LARGE
{
	uint32 addr;
    uint8 tdata[2];
    uint8 paddr[4];
    uint16 rdata;
	uint8 err;
    addr = page;
    addr <<= 20;
    addr |= offset;
    paddr[0] = (uint8)(addr<<1|0x01);
    paddr[1] = (uint8)(addr>>16);
    paddr[2] = (uint8)(addr>>24);
    paddr[3] = (uint8)(addr>>7);
    err = cdt_r_reg(RDA5880DM_CHIP_ID_1, paddr, 4, tdata, 2);
    rdata = tdata[0];
    rdata = rdata |(tdata[1] << 8);  
    return rdata;
}

U16 i2c1GetDataA(U32 addr)
{
    U8 tdata[2];
    U8 paddr[4];
    U16 rdata;

    paddr[0] = (addr<<1|0x01);
    paddr[1] = (addr>>16);
    paddr[2] = (addr>>24);
    paddr[3] = (addr>>7);

    cdt_r_reg(RDA5880DM_CHIP_ID_1, paddr, 4, tdata, 2);
    rdata = 0;
    rdata = tdata[0];
    rdata |= (tdata[1] << 8);   
    return rdata;
}

