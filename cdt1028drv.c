void rdaSeek(RDA_SCAN_CH_RES_T scan_res) //  //  // xiadd
{
	U16 tmp,tmp1,tmp_afc;
	
	#ifdef SEEK_DEBUG
	U32 addr_tmp=0;
	#endif
	S16 atv_top_cr_freq=0;
	U16 u16Afc = 0, u16Signal = 0, u16Noise = 0;
	//U16 u16hvTotal = 0;
	U8 fm_valid=0,fm_signal=0,tmp_flag=0;  /*afc=0,noise=0*/;
	U8 i;
	U8 u8i = 0, u8cnt = 5;
	U8 u8FMcheck_t = 0;
	U8 u8Hv_pass_times = 0;
	U8 u8fmRange = 4;
	


	 //  RDA_DELAY_MS(50);

	scan_res->vid_sta = VID_EMPTY_STA;
	scan_res->aud_sta = AUD_FALSE;
	scan_res->fm_stand = FM_NULL;
	
	 //  rdaDemodAtvStop();
	rdaDemodAtvReset();
	rdaDemodAtvSetEnv();
	rdaDemodAtvStart();
	 //  i2c1SendData(PAGE_ATV,0x10010,0x0080);     // 	rfpll_sdm reset

	RDA_DELAY_MS(40);

	tmp = i2c1GetData(PAGE_ATV,0x10022);
	tmp_flag = tmp&0x0080;     // get sign bit
	atv_top_cr_freq = (S8)tmp;
	if(tmp_flag)
	{
		atv_top_cr_freq = ~atv_top_cr_freq;
		atv_top_cr_freq = atv_top_cr_freq+1;
		atv_top_cr_freq = atv_top_cr_freq&0x00ff;
	}
	
	#ifdef SEEK_DEBUG
		 //  i2c1SendDataA(SCU_RAM_SEEK_CR_FREQ,atv_top_cr_freq);
	#endif
	
	tmp = atv_top_cr_freq&0x00ff;
	tmp = tmp*10;

	tmp_afc = i2c1GetDataA(0x00831f4c);
	 //  i2c1SendDataA(0x00c10035,0xb800);     //  for top seek lock   140605
	
    for(i = 0; i < 8; i++)
	{
		
		tmp1 = i2c1GetData(PAGE_ATV,0x10037);
		tmp1 = tmp1&0x0001; 
		#if TV_DEBUG
		printf("---->top seek lock: %d\n", tmp1);
		#endif
		if(tmp1)
		{
			break;
		}
		RDA_DELAY_MS(5);
	}
	
	
	#if 1
	if( tmp1 ==0)
	{
		scan_res->seek_incr = 6000;
		scan_res->vid_sta = VID_EMPTY_STA;
		return; 
	}
	#endif

	#ifdef SEEK_DEBUG
	//  i2c1SendDataA(SCU_RAM_SEEK_CR_LOCK,tmp1);
	#endif

	if(tmp1)
	{
		if(tmp > TH_MIN_CR_FREQ)
		{
			if(!tmp_flag)
			{
				 //  atv_top_cr_freq = atv_top_cr_freq&0xff;
				atv_top_cr_freq = -atv_top_cr_freq;
				 // atv_top_cr_freq = atv_top_cr_freq*10;
			}          
			scan_res->vid_sta = VID_NEEDRETUNE_STA;
			scan_res->seek_incr = atv_top_cr_freq*10;
            		goto seek_end;
		}
		else
		{
			scan_res->vid_sta = VID_YLOCKED_STA;
		}
	}
	else
	{
		scan_res->vid_sta = VID_NOTFINDPIC_STA;
		scan_res->seek_incr = 1500;
		goto seek_end;
	}
	
    RDA_DELAY_MS(150);



	//  check HV 
	u8cnt = 6;	
	u8Hv_pass_times = 0;
    for(tmp1=0;tmp1<u8cnt;tmp1++)
	{
		RDA_DELAY_MS(10);
		tmp = i2c1GetData(PAGE_FM_HV,0x10035);
		tmp = tmp&0x0fff;
		//u16hvTotal = u16hvTotal+tmp;
    //}
	//tmp = u16hvTotal /u8cnt;
    		//printf("---->HV: %x\n", tmp);
	 	if((tmp >= 610)&&(tmp <=645))     //  625 line mode   //     if(tmp >= 0x23f&&tmp <=0x285)     //  625 line mode
	   	{
	        	scan_res->line_num = LINE_625;
			scan_res->fm_stand = FM_I;
			tuner_cfg_g.hv_type_data.cnt_times_pal ++;
			u8Hv_pass_times ++;
			
			  //return;
			 //  break;
	    }
		else if((tmp >= 510)&&(tmp <=545))    // 0x1f4~0x226
	   	{
	        scan_res->line_num = LINE_525;
			scan_res->fm_stand = FM_MN;
			u8Hv_pass_times ++;
			tuner_cfg_g.hv_type_data.cnt_times_ntsc++;
			
			//return;
			//  break;
	    }
		else
		{
			//return;
		}
		if(u8Hv_pass_times >= 3)
		{
			scan_res->vid_sta = VID_FINDPIC_STA;
			return;
		}
		
	}
	#if 0
	if(tmp1>=u8cnt)
	{
		scan_res->vid_sta = VID_EMPTY_STA;
		return;
	}
	#endif
	
	addr_tmp = SCU_RAM_SEEK_FM_BASE;	
	
	//   FM setting
	for(u8FMcheck_t=0;u8FMcheck_t<u8fmRange;u8FMcheck_t++)
	{
		tmp1 = rdaTVMulDivCompensate(fm_freq_offset_tbl[u8FMcheck_t],20250,15);   //  u8FMcheck_t
		tmp1 = tmp1|0x8000;
		i2c1SendData(PAGE_FM_HV,0x10011,tmp1);
		RDA_DELAY_MS(150);

		 //  check FM afc
		u16Afc = 0;
		for(u8i = 0; u8i<u8cnt; u8i++)
		{
			RDA_DELAY_MS(2);
			tmp1 = i2c1GetData(PAGE_FM_HV,0x10020);     //  get fm_afc_abs
			tmp1 = tmp1&0x00ff;
			u16Afc += tmp1; 
		}
		tmp1= u16Afc/u8cnt;
		
		if(tmp1 < TH_MAX_FM_AFC)
		{
			fm_param[fm_valid].fm_afc = (U8)tmp1;
		}
		else
		{
			continue;
		}
		//  afc check end

		//  check  signal and noise
		u16Signal = 0;
		u16Noise = 0;
		for(u8i = 0; u8i<u8cnt; u8i++)
		{
			RDA_DELAY_MS(2);
			tmp = i2c1GetData(PAGE_FM_HV,0x10021);
			tmp1 = tmp&0x7f00;
			tmp1 = tmp1>>8;
			u16Signal += tmp1; 
			tmp1 = tmp&0x007f;
			u16Noise += tmp1;
		}
		u16Signal= u16Signal/u8cnt;
		u16Noise= u16Noise/u8cnt;
		fm_signal = u16Signal;

		
		if(fm_signal>u16Noise+10)
		{
			scan_res->vid_sta = VID_FINDPIC_STA;
			scan_res->fm_stand = FM_DK+u8FMcheck_t;
			
			fm_param[fm_valid].fm_signal= (U8)fm_signal;
			fm_param[fm_valid].fm_stand = FM_DK+u8FMcheck_t;
			fm_valid = fm_valid + 1;
			return;
			
		}
	}

	if(fm_valid == 0)
	{
		scan_res->fm_stand = FM_NULL;
		scan_res->aud_sta = AUD_FALSE;
	}
	else if(fm_valid == 1)
	{
		scan_res->fm_stand = fm_param[0].fm_stand;
		scan_res->aud_sta = AUD_TRUE;
	}
	else
	{
		scan_res->fm_stand = rdaScanFmConfirm(fm_valid);
		scan_res->aud_sta = AUD_TRUE;
	}

	if(scan_res->fm_stand == FM_NULL)
	{
		scan_res->seek_incr = 1500;
	}
	else
	{
		scan_res->seek_incr = 6000;
	}

	seek_end:
	;
}

uint8 CDT1028_CheckCurrentFreq(uint32 dwVFreq)LARGE
{
	uint8 status = CDTMTV_RT_SCAN_FAIL;
    rda_scan_ch_res scan_res;
    uint8 cnt = 3;
	tuner_cfg_g.tv_mode_index = RDA_ATV_SEEK;
	scan_res.u32Freq = dwVFreq;
    while(cnt)
    {
		rdaDemodAtvStop();
		rdaTVSetFreq_tune(dwVFreq,0,&tuner_cfg_g); //         rdaTVSetFreq_tune(dwVFreq,0,&tuner_cfg_g);
		i2c1SendData(PAGE_ATV,0x10010,0x0080);     // rfpll_sdm reset
        rdaSeek(&scan_res);  
        if(VID_FINDPIC_STA == scan_res.vid_sta)
        {
            if(scan_res.fm_stand != FM_NULL)
            {
                status = CDTMTV_RT_SCAN_DONE;
                fm_stand = scan_res.fm_stand;
                break;
            }
        }
        else if(VID_NEEDRETUNE_STA == scan_res.vid_sta)
		{			
			if((scan_res.seek_incr <260)&&(scan_res.seek_incr > -260))   //             if((scan_res.seek_incr < 260)&&(scan_res.seek_incr > -260))
            {
                dwVFreq += scan_res.seek_incr;
                cnt--;
            }
            else if((scan_res.seek_incr > (-1700))&&(scan_res.seek_incr <1700)) 
            {
		  		status = CDTMTV_RT_SCAN_FAIL;
				break;
            }
            else
            {
                status = CDTMTV_RT_SCAN_EMPTY_CH;
                break;
            }
		}
        else
        {
            if(6000 == scan_res.seek_incr)
            {
                status = CDTMTV_RT_SCAN_EMPTY_CH;
            }
            else
            {
                status = CDTMTV_RT_SCAN_EMPTY_CH;
            }    
            break;
        }
    }
   
    return status;
}


