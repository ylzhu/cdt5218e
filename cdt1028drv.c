{
    U16 tmp;
     // U8 Adac_vgain;
    tmp = i2c1GetData(PAGE_AFE_SDM, 0x1002A);
    tmp = (tmp & 0xF0FF) | 0x0900;    //tmp = (tmp & 0xF0FF) | 0x0F00; 
    i2c1SendData(PAGE_AFE_SDM, 0x1002A, tmp);

    tmp = i2c1GetData(PAGE_AFE_SDM, 0x1002C);
    tmp = (tmp & 0x0) | 0x0080;    //tmp = (tmp & 0xF0FF) | 0x0F00; 
    i2c1SendData(PAGE_AFE_SDM, 0x1002C, tmp);
    
    if(vol > 0x13)
        vol = 0x13;
    tmp = i2c1GetData(PAGE_FM_HV, 0x10019);
    tmp = (tmp & 0x0000)| vol;     // tmp = (tmp & 0xffc0)| vol;
    i2c1SendData(PAGE_FM_HV, 0x10019, tmp);  // i2c1SendData(PAGE_FM_HV, 0x10019, tmp);
}

